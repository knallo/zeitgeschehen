<h1>
	Kosten
</h1>
<p>
	Wir werden für die bis zu fünf Tage Unterkunft und Verpflegung zentral organisieren, deshalb sind wir auf einen Eigenbeitrag von um die 60-70 € pro Person angewiesen. Nur Getränke, die über Saft und Wasser hinaus gehen und teurerer Süßkram werden getrennt davon zum Selbstkostenpreis verkauft.
<p>
	Der von uns vorgeschlagene Beitrag von 60-70€ ist nicht kostendeckend, deshalb bitten wir euch, mehr zu bezahlen, falls ihr es euch leisten könnt. Falls ihr auch die 60-70€ nicht aufbringen könnt, soll dass aber kein Grund sein nicht zu kommen - dass alle Interessierten an den Workshops teilnehmen können, ist uns das Wichtigste. Gebt also einfach bei der Anmeldung an, wie viel Eigenbeitrag ihr euch leisten könnt.
	<br />
	<strong>
		Die finanzielle Lage soll kein Grund sein, nicht zu kommen!
	</strong>
</p>
