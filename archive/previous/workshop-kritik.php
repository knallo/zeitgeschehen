<h1 class="obertitel">Kritik – wie geht das?</h1>
<h2 class="untertitel">Eine Auseinandersetzung mit üblichen Formen des Kritisierens</h2>

<p>Die demokratische Kultur wird dafür geschätzt, dass da Bürger laufend nicht nur privat, sondern auch öffentlich Kritik üben, unablässig eine bessere Welt vermissen und fordern. Die stellt sich deshalb aber nicht ein, so dass ein ansehnliches Standardrepertoire von Beschwerden fortlebt.</p>
<p>Das heißt leider nicht, dass die Beschwerde führenden Bürger beherrschen, wie Kritik geht. Ihnen unterlaufen immerzu die gleichen Fehler, durch die sie nicht nur das zunächst einmal theoretische Gewerbe des Kritisierens verpfuschen. Kritiker von rechts, links und aus der Mitte pflegen auch die Sitte der konstruktiven Kritik, ganz als ob es selbstverständlich wäre, dass aus Einwänden nie die Ablehnung des Kritisierten, sondern immer die Sorge um seine Verbesserung zu folgen habe. Damit bilden sie den Willen zum Mitmachen aus – bei allem, was ihnen dauernd missfällt.</p>
<p>Zur selben Blüte gelangen gewisse Standards der Kritik von Kritik – bis hin zum Verbot…
Im Workshop wollen wir der Frage nachgehen, welchen Fehlern es zu „verdanken“ ist, dass im üblichen Beschwerdewesen Kritik regelmäßig bei der Bekräftigung des Kritisierten landet.</p>

