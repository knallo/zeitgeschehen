<h1 class="obertitel">Klimawandel und Energiewende</h1>

<p>Seit Paris stellen sich fast alle Staaten ihrer Verantwortung für dieses Menschheitsproblem! Ungeachtet aller Katastrophenszenarien schicken die Industrienationen unvermindert Treibhausgase in die Atmosphäre. Ein eklatantes Versagen aller Staatenlenker, die ihre Verantwortung für den blauen Planeten den Lobby-Interessen der Wirtschaft opfern?</p>
<p>Nein. Die Staatenlenker definieren das Menschheitsproblem anders, wenn sie mit dem Klimawandel kalkulieren: Manche Politiker nehmen die Störungen ins Visier, die entwurzelte und fliehende Menschen ihnen, ihren geordneten Verhältnissen antun könnten; andere rechnen mit der Chance eines Umbaus der Energieerzeugung für die nationale Wirtschaft, der Menschheit neue Umwelttechnik zu verkaufen.</p>
<p>Ist das Menschheitsproblem Klima am Ende gar nicht das - ewig verratene - Ziel, sondern ein wohlklingender Zuständigkeitsanspruch für eine deutsche Exportoffensive? Ist der Kampf um die internationale Verankerung von Klimazielen und für eine emissionsarme Wirtschaftsweise ein Mittel für nationale Wachstumsoffensiven, und ist der Dauer-Streit um diese Klimaziele und ihre Umsetzung die Konkurrenz der Nationen darum?</p>

