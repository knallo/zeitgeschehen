<h1 class="obertitel">Bedingungsloses Grundeinkommen</h1>

<p>Das bedingungslose Grundeinkommen ist ein öffentliches Thema. Linke Befürworter des Konzepts von Attac oder der Partei DIE LINKE rühren die Werbetrommel. Die Losung der Verfechter des Grundeinkommens lautet: „Wir produzieren genug für alle. Trotzdem leiden viele Menschen unter finanzieller und materieller Armut. Das muss nicht sein!“ Stimmt: Reichlich gefüllte Schaufenster und Warenlager und auf der anderen Seite immer mehr Menschen, die ohne und mit Arbeit mit einem Existenzminimum auskommen müssen.</p>
<p>Aber warum ist das so?</p>
<p>Diese Frage stellen die Freunde des bedingungslosen Grundeinkommens nicht: Was ist diese Produktion von Reichtum, wenn als deren Resultat die Menschen, die die Warenreichtum herstellen, unter Existenzsorgen leiden oder ganz ihre Einkommen verlieren? </p>
<p>Vielmehr denken sie sich die kapitalistische Welt als Ansammlung von Chancen zu ihrer Verbesserung und befassen sich in immer neuen Varianten mit der Frage: Wo soll das Geld herkommen, das der Staat als bedingungslosen Grundeinkommens umverteilen soll? Ihre Antworten kommen einem sehr bekannt vor: Waren müssen produziert und natürlich mit Gewinn verkauft, Erwerbsarbeit muß geleistet, Steuern müssen bei erfolgreich Verdienenden abkassiert werden, usw.</p>

