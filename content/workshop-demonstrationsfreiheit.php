<h1 class="obertitel">Über die Freiheit zum Demonstrieren</h1>
<h2 class="untertitel">oder: Warum mündet die Wahrnehmung dieses Grundrechts regelmäßig in eine Gewaltdebatte?</h2>


<p>Das Grundgesetz erlaubt neben der Meinungs- auch die <em>Versammlungsfreiheit</em>, kurz: billigt den Bürgern ein Demonstrationsrecht zu. Beide Freiheiten zählen zu den Höchstwerten der Demokratie, an denen die Insassen der Demokratie ihre Freude haben sollen. Sehr sorgfältig wird folglich staatlicherseits auf die Demonstrationsfreiheit aufgepasst, weswegen man nach öffentlichen Protestaktionen aus den Medien auch regelmäßig mehr über Polizeieingriffe, Widerstand, Verhaftungen und Verwüstungen als über die kritischen Anliegen von Demonstranten erfährt.</p>

<p><em>Dass</em> die z.B. irgendetwas gegen AKWs haben, TTIP nicht leiden können, eine andere Klimapolitik wollen oder dem G20-Gipfel wenig gewogen sind, wird nicht und lässt sich kaum verschweigen. <em>Worin</em> jedoch ihre Kritik an AKWs, Handelsabkommen, Klimapolitik oder dem globalen Wirken der Weltmächte besteht – <em>Fehlanzeige</em>. Allerdings ist ebenso festzuhalten: Gelegentlich sehen sich Demonstranten auf besondere Weise „belohnt“, etwa wenn aus einer Anti-AKW-Bewegung ein Umweltministerium wird! Ob dort die Anliegen der Demonstranten zur Geltung kommen – <em>Fragezeichen</em>.</p>

<p><em>Selbstverständlich</em> ist an diesem Grundrecht nebst der bornierten Sorte öffentlicher Aufmerksamkeit also nichts:</p>

<ul>
	<li>Warum bedarf es überhaupt einer <em>staatlichen Erlaubnis</em> für nichts anderes, als sich „unter freiem Himmel friedlich zu versammeln“ - wie es im Grundgesetz heißt?</li>
	<li>Warum wird den Kritikern per Grundgesetz <em>erlaubt</em>, ihre abweichende Meinung demonstrativ öffentlich vorzutragen, wenn es dann auf den Inhalt des Protests für sich gar <em>nicht ankommt</em>?</li>
	<li>Warum gilt das Grundrecht auf Demonstrationsfreiheit als eines der <em>Höchstwerte</em> der Demokratie, wenn in der Öffentlichkeit primär von Interesse ist, wie <em>friedlich</em> sich die Demonstranten&nbsp;beim Protestieren <em>aufführen</em>?</li>
</ul>

<p>Grund genug, sich einmal mit der Frage zu befassen, welche <em>politischen Zwecke</em> mit diesem Grundrecht verfolgt werden.</p>


